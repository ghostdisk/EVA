VersionChain* GetVersionChain(const std::string& name) {
	for (VersionChain* chain : g_versionChains) {
		if (chain->typeName == name)
			return chain;
	}
	VersionChain* chain = new VersionChain();
	chain->typeName = name;
	g_versionChains.push_back(chain);

	return chain;
}

static CXChildVisitResult RecordChildVisitor(CXCursor cursor, CXCursor, void* _type) {
	Type* type = (Type*)_type;
	CXCursorKind kind = clang_getCursorKind(cursor);

	if (kind == CXCursor_AnnotateAttr) {
		std::string annotation = Str(clang_getCursorSpelling(cursor));
		std::vector<std::string> args = SplitString(annotation, ',');
		for (std::string& arg : args)
			arg = TrimString(arg);

		if (args[0] == "eversion") {
			type->version = std::stoi(args[1]);
			if (args.size() >= 3)
				type->versionChain = GetVersionChain(args[2]);
			else
				type->versionChain = GetVersionChain(type->symbolName);
			type->versionChain->Set(type->version, type);
		}
	} else if (kind == CXCursor_CXXBaseSpecifier) {
		CXType base_type = clang_getCanonicalType(clang_getCursorType(cursor));
		type->parent = GetType(StripRecordKeyword(Str(clang_getTypeSpelling(base_type))));
		type->parent->subclasses.push_back(type);
	} else if (kind == CXCursor_FieldDecl) {
		// clang_visitChildren(cursor, FieldAnnotationVisitor, &is_prop);
			type->fields.push_back(Field {
			.type = "", // TODO
			.name = Str(clang_getCursorSpelling(cursor)),
		});
	} else if (kind == CXCursor_EnumConstantDecl) {
		type->enumValues.push_back(EnumValue {
			.string = Str(clang_getCursorSpelling(cursor)),
			.value = clang_getEnumConstantDeclValue(cursor),
		});
	}
	return CXChildVisit_Continue;
}

static CXChildVisitResult Visit(CXCursor cursor, CXCursor parent, void* userdata) {
	CXCursorKind kind = clang_getCursorKind(cursor);
	if (kind == CXCursor_FunctionDecl && IsInOurTree(cursor))
		CollectAuto(cursor);

	bool is_record = kind == CXCursor_StructDecl || kind == CXCursor_ClassDecl;
	bool isEnum   = kind == CXCursor_EnumDecl;

	// only actual definitions, and only ours:
	if ((is_record || isEnum) && clang_isCursorDefinition(cursor) && IsInOurTree(cursor)) {
		CXSourceLocation loc = clang_getCursorLocation(cursor);
		CXFile file;
		unsigned line = 0;
		clang_getFileLocation(loc, &file, &line, nullptr, nullptr);

		std::string name          = Str(clang_getCursorSpelling(cursor));
		std::string qualifiedName = StripRecordKeyword(Str(clang_getTypeSpelling(clang_getCursorType(cursor))));
		std::string filename      = ToForwardSlashes(Str(clang_getFileName(file)));

		Type* type = GetType(qualifiedName);
		clang_visitChildren(cursor, RecordChildVisitor, type);
		type->isAbstract = clang_CXXRecord_isAbstract(cursor);
		type->isObject = qualifiedName == "Object" || (type->parent && type->parent->isObject);
		type->header = RelToBase(filename);
		type->isEnum = isEnum;

		if (type->isObject) {
			g_typesWeCareAbout.push_back(type);
			g_includes.push_back(filename);
		}

		if (isEnum) 
			type->enumUnderlyingType = Str(clang_getTypeSpelling(clang_getEnumDeclIntegerType(cursor)));
	}

	clang_visitChildren(cursor, Visit, nullptr);
	return CXChildVisit_Continue;
}

static void WriteOutput() {
	fs::path out = fs::path(g_baseDir) / "EVA" / "EVA.gen.cpp";

	FILE* f = fopen(out.string().c_str(), "wb");
	if (!f) {
		fprintf(stderr, "evagen: failed to open %s for writing\n", out.string().c_str());
		return;
	}
	

	for (Type* type : g_types)
		if (type->shouldEmitTypeInfo)
			g_typesWeCareAbout.push_back(type);

	fprintf(f, "// GENERATED BY EVAGEN. DO NOT EDIT.\n");
	for (const std::string& inc : g_includes) {
		fprintf(f, "#include <%s>\n", inc.c_str());
	}
	fprintf(f, "\n");

	for (Type* type : g_typesWeCareAbout) {
		fprintf(f, "extern %s g_type_%s;\n", type->isEnum ? "EnumType" : "Type", type->symbolName.c_str());
	}
	fprintf(f, "\n//------------------------------------------------------------\n\n");

	for (Type* type : g_typesWeCareAbout) {
		std::string parent_ref = type->parent && type->parent->isObject ?  ("&g_type_" + type->parent->symbolName) : "nullptr";
		if (type->isEnum) {
			fprintf(f, "EnumType g_type_%s {\n", type->symbolName.c_str());
			fprintf(f, "\t\"%s\",\n", type->qualifiedName.c_str());
			fprintf(f, "\t{\n");
			for (const EnumValue& value : type->enumValues)
				fprintf(f, "\t\t{ \"%s\", %lld },\n", value.string.c_str(), value.value);
			fprintf(f, "\t},\n");
			fprintf(f, "};\n");
			continue;
		}

		fprintf(f, "Type g_type_%s = {\n", type->symbolName.c_str());

		fprintf(f, "\t.name = \"%s\",\n", type->qualifiedName.c_str());
		fprintf(f, "\t.parent_type = %s,\n", parent_ref.c_str());

		if (type->subclasses.size()) {
			fprintf(f, "\t.subclasses = {\n");
			for (Type* subclass : type->subclasses) {
				fprintf(f, "\t\t&g_type_%s,\n", subclass->symbolName.c_str());
			};
			fprintf(f, "\t},\n");
		}
		if (type->isObject && !type->isAbstract) {
			fprintf(f,
				"\t.Instantiate = [](Allocator allocator) -> void* {\n"
				"\t\tvoid* ptr = (void*)allocator.Allocate(sizeof(%s), alignof(%s));\n"
				"\t\tnew (ptr) %s();\n"
				"\t\treturn ptr;\n"
				"\t},\n",
				type->qualifiedName.c_str(), type->qualifiedName.c_str(), type->qualifiedName.c_str());
		}
		fprintf(f, "};\n");
	}

	fprintf(f, "\n//------------------------------------------------------------\n\n");

	for (Type* type : g_typesWeCareAbout) {
		if (!type->isEnum || !type->HasAuto("ToString"))
			continue;

		fprintf(f, "ZTString ToString(%s& t) {\n", type->qualifiedName.c_str());
		fprintf(f, "\tfor (const EnumValue& value : g_type_%s.values)\n", type->symbolName.c_str());
		fprintf(f, "\t\tif (value.value == (I64)t) return value.string;\n");
		fprintf(f, "\treturn {};\n");
		fprintf(f, "}\n");

		fprintf(f, "template<> %s FromString<%s>(String str) {\n", type->qualifiedName.c_str(), type->qualifiedName.c_str());
		fprintf(f, "\tfor (const EnumValue& value : g_type_%s.values)\n", type->symbolName.c_str());
		fprintf(f, "\t\tif (value.string == str) return (%s)value.value;\n", type->qualifiedName.c_str());
		fprintf(f, "\treturn {};\n");
		fprintf(f, "}\n");

		if (type->HasAuto("Serialize")) {
			fprintf(f, "void Serialize(Serializer& s, const %s& value) {\n", type->qualifiedName.c_str());
			fprintf(f, "\tif (s.IsText()) {\n");
			fprintf(f, "\t\t%s textValue = value;\n", type->qualifiedName.c_str());
			fprintf(f, "\t\ts.SerializeString(ToString(textValue));\n");
			fprintf(f, "\t} else {\n");
			fprintf(f, "\t\t%s underlyingValue = (%s)value;\n", type->enumUnderlyingType.c_str(), type->enumUnderlyingType.c_str());
			fprintf(f, "\t\tSerialize(s, underlyingValue);\n");
			fprintf(f, "\t}\n");
			fprintf(f, "}\n");

			fprintf(f, "void Deserialize(Deserializer& d, %s& value) {\n", type->qualifiedName.c_str());
			fprintf(f, "\tif (d.IsText()) {\n");
			fprintf(f, "\t\tvalue = FromString<%s>(d.DeserializeString());\n", type->qualifiedName.c_str());
			fprintf(f, "\t} else {\n");
			fprintf(f, "\t\t%s underlyingValue = {};\n", type->enumUnderlyingType.c_str());
			fprintf(f, "\t\tDeserialize(d, underlyingValue);\n");
			fprintf(f, "\t\tvalue = (%s)underlyingValue;\n", type->qualifiedName.c_str());
			fprintf(f, "\t}\n");
			fprintf(f, "}\n");
		}
	}

	fprintf(f, "\n//------------------------------------------------------------\n\n");

	for (Type* type : g_types) {
		if (type->isObject) {
			fprintf(f, "Type* %s::StaticClass() { return &g_type_%s; }\n", type->qualifiedName.c_str(), type->symbolName.c_str());
			fprintf(f, "Type* %s::GetClass() { return &g_type_%s; }\n", type->qualifiedName.c_str(), type->symbolName.c_str());
		}
	}
	fprintf(f, "\n//------------------------------------------------------------\n\n");

	fprintf(f, "Type* Type::Find(String name) {\n");
	fprintf(f, "\tstatic Type* g_allTypes[] = {\n");
	for (Type* t : g_typesWeCareAbout) {
		fprintf(f, "\t\t&g_type_%s,\n", t->symbolName.c_str());
	}
	fprintf(f, "\t};\n");
	fprintf(f, "\tfor (Type* t : g_allTypes)\n");
	fprintf(f, "\t\tif (name == t->name) return t;\n");
	fprintf(f, "\treturn nullptr;\n");
	fprintf(f, "}\n");

	fprintf(f, "\n//------------------------------------------------------------\n\n");

	for (VersionChain* chain : g_versionChains) {
		Type* latest = chain->versions[chain->versions.size() - 1];
		if (!latest->HasAuto("Serialize"))
			continue;

		for (int i = 1; i < chain->versions.size(); i++) {
			Type* current = chain->versions[i];
			Type* prev = chain->versions[i - 1];

			if (current && prev) {
				fprintf(f, "void Convert(const %s& oldValue, %s& newValue);\n", prev->qualifiedName.c_str(), current->qualifiedName.c_str());
			}
		}
	}
	fprintf(f, "\n");

	for (VersionChain* chain : g_versionChains) {
		Type* latest = chain->versions[chain->versions.size() - 1];
		if (!latest->HasAuto("Serialize"))
			continue;

		fprintf(f, "void Serialize(Serializer& s, const %s& value) {\n", latest->qualifiedName.c_str());
		fprintf(f, "	s.BeginObject();\n");
		fprintf(f, "	s.Key(\"version\");\n");
		fprintf(f, "	s.SerializeU32(%u);\n", latest->version);

		for (const Field& field : latest->fields) {
			fprintf(f, "	s.Key(\"%s\");\n", field.name.c_str());
			fprintf(f, "	Serialize(s, value.%s);\n", field.name.c_str());
		}
		fprintf(f, "	s.EndObject();\n");
		fprintf(f, "}\n");


		fprintf(f, "void Deserialize(Deserializer& d, %s& v%u) {\n", latest->qualifiedName.c_str(), latest->version);
		fprintf(f, "	d.BeginObject();\n");
		fprintf(f, "	d.Key(\"version\");\n");
		fprintf(f, "	U32 version = d.DeserializeU32();\n");
		fprintf(f, "	switch (version) {\n");

		for (Type* type : chain->versions) {
			if (!type) continue;

			fprintf(f, "		case %u: {\n", type->version);

			for (int v = type->version; v < chain->versions.size() - 1; v++) {
				fprintf(f, "			%s v%u;\n", chain->versions[v]->qualifiedName.c_str(), v);
			};
			for (const Field& field : type->fields) {
				fprintf(f, "			d.Key(\"%s\");\n", field.name.c_str());
				fprintf(f, "			Deserialize(d, v%u.%s);\n", type->version, field.name.c_str());
			}
			for (int v = type->version; v < chain->versions.size() - 1; v++) {
				fprintf(f, "			Convert(v%u, v%u);\n", v, v + 1);
			};

			fprintf(f, "			break;\n");
			fprintf(f, "		}\n");
		}
		fprintf(f, "	};\n");

		fprintf(f, "	d.EndObject();\n");
		fprintf(f, "}\n");

	}

	fclose(f);
}

int main() {
	std::vector<std::string> headers = CollectHeaders();

	printf("evagen: collected %zu headers\n", headers.size());
	std::string amalgam = WriteAmalgam(headers);

	std::string base = g_baseDir;
	std::vector<std::string> arg_strs = {
		"-x", "c++",
		"-std=c++20",
		"-DEVAGEN",
		"-I" + base,
		"-I" + base + "/Vendor/cglm-0.9.6/include",
		"-I" + base + "/Vendor/box3d/include",
		"-I" + base + "/Vendor/SDL-release-3.4.10/include",
		"-I" + base + "/Vendor/freetype-2.14.3/include",
		"-I" + base + "/Vendor/enet-1.3.18/include",
		"-I" + base + "/Vendor/volk",
	};

	if (const char* vulkanSdk = getenv("VULKAN_SDK"))
		arg_strs.push_back("-I" + std::string(vulkanSdk) + "/Include");
	std::vector<const char*> args;
	for (const std::string& s : arg_strs) args.push_back(s.c_str());

	CXIndex index = clang_createIndex(0, 0);

	CXTranslationUnit translationUnit = nullptr;
	CXErrorCode err = clang_parseTranslationUnit2(
		index, amalgam.c_str(), args.data(), (int)args.size(),
		nullptr, 0, CXTranslationUnit_SkipFunctionBodies | CXTranslationUnit_KeepGoing, &translationUnit);

	if (err != CXError_Success || !translationUnit) {
		fprintf(stderr, "evagen: failed to parse translation unit (err=%d)\n", (int)err);
		exit(1);
	}

	// Surface parse errors so we notice when the amalgam doesn't compile.
	unsigned num_diags = clang_getNumDiagnostics(translationUnit);
	bool hasCompileError = false;
	for (unsigned i = 0; i < num_diags; ++i) {
		CXDiagnostic diag = clang_getDiagnostic(translationUnit, i);
		CXDiagnosticSeverity sev = clang_getDiagnosticSeverity(diag);
		if (sev >= CXDiagnostic_Error) {
			std::string text = Str(clang_formatDiagnostic(diag, clang_defaultDiagnosticDisplayOptions()));
			fprintf(stderr, "  %s\n", text.c_str());
			hasCompileError = true;
		}
		clang_disposeDiagnostic(diag);
	}
	if (hasCompileError) exit(1);

	CXCursor root = clang_getTranslationUnitCursor(translationUnit);
	clang_visitChildren(root, Visit, nullptr);
	FinalizeAutos();

	WriteOutput();
	return 0;
}
