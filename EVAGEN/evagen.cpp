struct VersionChain;
struct Type;

struct Field {
	std::string type;
	std::string name;
};

struct EnumValue {
	std::string string;
	long long   value;
};

struct Type {
	std::string              name                  = "";
	std::string              qualifiedName         = "";
	std::string              symbolName            = "";
	Type*                    parent                = nullptr;
	std::vector<Type*>       subclasses            = {};
	std::string              header                = "'";
	std::vector<Field>       fields                = {};
	bool                     isObject              = false;
	bool                     isAbstract            = false;
	bool                     isEnum                = false;
	std::string              enumUnderlyingType    = "";
	std::vector<EnumValue>   enumValues            = {};
	std::vector<std::string> autos                 = {};
	int                      version               = 0;
	VersionChain*            versionChain          = nullptr;
	bool                     shouldEmitTypeInfo    = false;

	bool HasAuto(const std::string& name) const {
		return std::find(autos.begin(), autos.end(), name) != autos.end();
	}
};

struct VersionChain {
	std::string typeName;
	std::vector<Type*> versions;

	void Set(int version, Type* type) {
		if (versions.size() <= version) {
			versions.resize(version + 1);
		}
		versions[version] = type;
	}
};

